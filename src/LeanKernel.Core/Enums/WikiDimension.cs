namespace LeanKernel.Core.Enums;

/// <summary>
/// Represents the available wiki dimension values.
/// </summary>
public enum WikiDimension
{
    /// <summary>
    /// People, organizations, or other actors.
    /// </summary>
    Who,

    /// <summary>
    /// Events, actions, tasks, or concepts.
    /// </summary>
    What,

    /// <summary>
    /// Locations or environments.
    /// </summary>
    Where,

    /// <summary>
    /// Dates, times, schedules, or temporal facts.
    /// </summary>
    When,

    /// <summary>
    /// Reasons, motivations, or causes.
    /// </summary>
    Why,

    /// <summary>
    /// Processes, methods, or procedures.
    /// </summary>
    How
}
